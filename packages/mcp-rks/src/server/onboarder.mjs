const STATE_FILE = ".rks/onboarder-state.json";
const PROJECT_JSON = ".rks/project.json";

// ─── State helpers ────────────────────────────────────────────────────────────

export function getOnboarderState(projectRoot) {
  const statePath = path.join(projectRoot, STATE_FILE);
  if (!fs.existsSync(statePath)) {
    const now = Date.now();
    return {
      version: 1,
      startedAt: now,
      lastTouchedAt: now,
      completedStages: [],
      currentStage: "welcome",
      projectId: null,
    };
  }
  try {
    return JSON.parse(fs.readFileSync(statePath, "utf8"));
  } catch {
    const now = Date.now();
    return {
      version: 1,
      startedAt: now,
      lastTouchedAt: now,
      completedStages: [],
      currentStage: "welcome",
      projectId: null,
    };
  }
}

export function saveOnboarderState(projectRoot, state) {
  const statePath = path.join(projectRoot, STATE_FILE);
  const updated = { ...state, lastTouchedAt: Date.now() };
  fs.mkdirSync(path.dirname(statePath), { recursive: true });
  fs.writeFileSync(statePath, JSON.stringify(updated, null, 2), "utf8");
  return updated;
}

function readProjectJson(projectRoot) {
  const p = path.join(projectRoot, PROJECT_JSON);
  if (!fs.existsSync(p)) return {};
  try { return JSON.parse(fs.readFileSync(p, "utf8")); } catch { return {}; }
}

function writeProjectJson(projectRoot, data) {
  const p = path.join(projectRoot, PROJECT_JSON);
  fs.mkdirSync(path.dirname(p), { recursive: true });
  fs.writeFileSync(p, JSON.stringify(data, null, 2), "utf8");
}

// ─── File generation ──────────────────────────────────────────────────────────

export function generateProjectFiles(projectRoot, projectData = {}) {
  const notesDir = path.join(projectRoot, "notes");
  if (!fs.existsSync(notesDir)) {
    fs.mkdirSync(notesDir, { recursive: true });
  }

  const projectId = projectData.projectId || path.basename(projectRoot);
  const oneLiner = projectData.one_liner || `${projectId} — add a description here.`;
  const typeDesc = projectData.type_description || null;
  const timestamp = Date.now();

  const overviewPath = path.join(notesDir, "project.overview.md");
  const overviewExists = fs.existsSync(overviewPath);
  if (!overviewExists) {
    fs.writeFileSync(overviewPath, `---
id: project.overview
title: "Project Overview"
desc: "${oneLiner}"
created: ${timestamp}
updated: ${timestamp}
---

# ${projectId}

## About This Project

${oneLiner}
${typeDesc ? `\n${typeDesc}\n` : ""}`, "utf8");
  }

  const agentsPath = path.join(projectRoot, "AGENTS.md");
  const agentsExists = fs.existsSync(agentsPath);
  if (!agentsExists) {
    fs.writeFileSync(agentsPath, `# AGENTS.md

<!-- Generated by rks onboarder. Edit freely — this is your project's context file. -->

## About

${projectId} — ${oneLiner}

## Build & Test

\`\`\`bash
# Add your build and test commands here
\`\`\`

## Architecture

<!-- Describe your project's key directories and entry points -->

## Conventions

<!-- Add coding standards, naming conventions, or patterns specific to this project -->

## Git Workflow

Branch from \`staging\`. PR into \`staging\`. Squash merge.
Branch naming: \`feature/\`, \`fix/\`, \`refactor/\`, \`docs/\`, \`chore/\`
`, "utf8");
  }

  return { overviewCreated: !overviewExists, agentsCreated: !agentsExists };
}

// ─── Rate card and cost summary ──────────────────────────────────────────────

export const RATE_CARD = {
  "claude-opus-4-7":    { input: 15 / 1e6,    output: 75 / 1e6 },
  "claude-sonnet-4-6":  { input: 3 / 1e6,     output: 15 / 1e6 },
  // Haiku 4.5 is $1/$5 per 1M — NOT the old Haiku 3.5 $0.25/$1.25.
  "claude-haiku-4-5":   { input: 1 / 1e6,     output: 5 / 1e6 },
};

function buildCostSummary(buildData) {
  if (!buildData) return "not recorded";
  const { rawCost, inputTokens, outputTokens, modelName } = buildData;

  if (rawCost != null) {
    return `~$${rawCost.toFixed(4)} (Approximate; actual cost depends on your Anthropic plan)`;
  }

  const rate = modelName ? RATE_CARD[modelName] : null;
  if (inputTokens != null && rate) {
    const dollars = inputTokens * rate.input + (outputTokens || 0) * rate.output;
    return `~$${dollars.toFixed(4)} via ${modelName} (Approximate; actual cost depends on your Anthropic plan)`;
  }

  if (inputTokens != null) {
    const total = inputTokens + (outputTokens || 0);
    return `${total} tokens via ${modelName || "unknown model"} (rate card unavailable)`;
  }

  return "not recorded";
}

