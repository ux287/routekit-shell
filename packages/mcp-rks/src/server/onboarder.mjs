function emit(projectRoot, slug, metrics = {}) {
  try {
    recordTelemetry(projectRoot, { slug, metrics, timestamp: Date.now() });
  } catch {
    // telemetry is best-effort
  }
}

// ─── Stage handlers ───────────────────────────────────────────────────────────

function makePayload(stage, state, display = "", extra = {}) {
  return { ok: true, stage, display, prompts: [], nextAction: {}, state, ...extra };
}

function handleWelcome(state, opts, projectRoot) {
  const { bounce } = opts || {};
  const responses = opts?.responses || {};

  // Capture verbosityChoice if provided on this or prior call
  const verbosityChoice = responses.verbosityChoice;
  if (verbosityChoice !== undefined) {
    if (!state.responses) state.responses = {};
    state.responses.verbosityChoice = verbosityChoice;
    if (verbosityChoice === "yes") {
      state.sessionVerbose = true;
    }
  }

  if (bounce) {
    emit(projectRoot, "onboarder.stage.skipped", { stage: "welcome", reason: "bounced" });
    const proj = readProjectJson(projectRoot);
    proj.onboarder = { ...(proj.onboarder || {}), dismissed: true };
    writeProjectJson(projectRoot, proj);
    state.dismissed = true;
    return makePayload("welcome", state,
      "Thanks for trying rks. If you change your mind, `/rks-onboard --reset`. — UX287."
    );
  }

  const isFreshSession = !state.completedStages || state.completedStages.length === 0;
  if (isFreshSession) {
    emit(projectRoot, "onboarder.session.started", {
      projectId: state.projectId,
      version: state.version,
      source: "auto-trigger",
    });
  }

  emit(projectRoot, "onboarder.stage.started", { stage: "welcome" });
  const t0 = Date.now();

  // Verbosity detection
  const proj = readProjectJson(projectRoot);
  const verbosity = proj.verbosity || process.env.RKS_VERBOSITY || "verbose";
  const alreadyVerbose = verbosity === "verbose";
  const verbosityPrompt = alreadyVerbose
    ? ""
    : `\n\nOne note before we continue: your project verbosity is set to \`${verbosity}\`. Stages 4-6 are designed to show the Governor in action — tool calls, plans, and results live. Should we switch to verbose mode for this onboarding session? (Type **yes** to enable for this session, or **no** to continue as-is)`;

  const display = `Welcome. You're in routekit-shell — rks for short.

Here is what this is, in one paragraph:

rks is a hooked-agent framework: it runs your AI agent inside hook-enforced lanes, with a local knowledge graph, audit trail, and structured pipeline for every unit of work. Every change is either a **story** — a small piece of work with a defined scope and tests — or a **framework update** to rks itself. The agent doesn't freelance. It writes a plan, you see the plan, the plan is enforced as it runs, and every commit ships through a transparent pipeline.

The primary use case today is software development — governed AI-driven code changes with cost visibility and scope enforcement. The framework is general enough to apply anywhere you want governed agents, structured audit trails, and a local knowledge graph: research workflows, document automation, data pipelines.

If you've worked in any domain where an AI agent ran off-script and left a mess behind — rks is the response to that.

The next six stages take about ten minutes. We'll set expectations, walk through the permission model, and then write and ship a real first story together. By the end you'll have a PR open with a real cost report attached.

If that's not what you want — if you came looking for a freeform AI environment — this is the right moment to know that's not what rks is. Type \`/rks-onboard --bounce\` and we'll part ways cleanly.${verbosityPrompt}

Ready to continue? Type **yes** (or any response) to move to Stage 2, or \`/rks-onboard --bounce\` to exit.`;

  if (!state.completedStages.includes("welcome")) {
    state.completedStages.push("welcome");
  }
  generateProjectFiles(projectRoot, { projectId: state.projectId });

  emit(projectRoot, "onboarder.stage.completed", {
    stage: "welcome",
    durationSeconds: Math.round((Date.now() - t0) / 1000),
  });

  return makePayload("welcome", state, display);
}

