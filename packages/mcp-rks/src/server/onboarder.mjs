const DEMO_PROBLEM_STATEMENT =
  "Add a Hello World React component to src/components/Hello.tsx";

function emitGovernorError(projectRoot, stage, governorResult, state) {
  emit(projectRoot, "onboarder.stage.failed", {
    stage,
    failureReason: "governor_error",
    governorError: governorResult.error || "unknown",
  });
  return makePayload(
    stage,
    state,
    `The Governor encountered an error during Stage ${stage}: ${governorResult.error || "unknown error"}\n\nCheck \`runs/\` in your project directory for the Governor session log, or refer to \`notes/how-to.child-project-kickoff.md\` for manual steps.`,
    { ok: false, error: governorResult.error || "governor_error" }
  );
}

function handleFirstStory(state, opts, projectRoot) {
  const responses = opts?.responses || {};
  const userInput = responses.userInput;
  const governorResult = responses.governorResult;

  if (governorResult) {
    if (governorResult.ok === false || governorResult.error) {
      return emitGovernorError(projectRoot, "first_story", governorResult, state);
    }

    emit(projectRoot, "onboarder.stage.started", { stage: "first_story" });
    const t0 = Date.now();

    const storyId = governorResult.storyId;
    if (!state.responses) state.responses = {};
    if (!state.responses.first_story) state.responses.first_story = {};
    state.responses.first_story.storyId = storyId;
    state.responses.first_story.isDemo = !!responses.isDemo;

    if (!state.completedStages.includes("first_story")) {
      state.completedStages.push("first_story");
    }

    emit(projectRoot, "onboarder.stage.completed", {
      stage: "first_story",
      storyId,
      durationSeconds: Math.round((Date.now() - t0) / 1000),
    });

    const display = `Story \`${storyId}\` has been written by the Product Owner Governor.

**A quick note on trust.** The hooks running during this session are open source at \`.routekit/hooks/\`. Every tool call the agent made is logged verbatim to \`runs/\` in your project directory. No data leaves your machine. The MCP server is open source under AGPL-3.0.

Type **build** to move to Stage 5 — the Build Governor will implement your story.`;

    return makePayload("first_story", state, display, { storyId });
  }

  const isDemo = userInput === "demo";
  const problemStatement = isDemo ? DEMO_PROBLEM_STATEMENT : (userInput || null);

  // Input received (a problem statement or `demo`): ACKNOWLEDGE it and dispatch the PO — do NOT
  // re-render the bare Stage 4 prompt with an empty nextAction (that read as "stuck" in UAT F2).
  // first_story is recorded on the follow-up call once the PO returns a governorResult (branch above).
  // (backlog.fix.onboarder-demo-stage-stuck)
  if (problemStatement) {
    const label = isDemo ? 'the "Hello Routekit!" demo story' : "your problem statement";
    const display = `Stage 4 of 7 — Your First Story.

Got it — dispatching the Product Owner Governor now to turn ${label} into a full story (acceptance criteria, scope, tests). This runs \`/po\`.

When it returns, type **build** to move to Stage 5.`;
    const result = makePayload("first_story", state, display);
    result.pendingDispatch = { type: "po", problemStatement, isDemo };
    result.nextAction = { type: "dispatch", governor: "po", problemStatement, isDemo };
    return result;
  }

  // No input yet — show the Stage 4 prompt. The demo option no longer assumes an init step the
  // tour never runs, and guards the framework-vs-project boundary (the demo writes real files).
  const display = `Stage 4 of 7 — Your First Story.

What problem do you want to solve in your project? Write one or two sentences. The Product Owner Governor will turn it into a full story with acceptance criteria, scope, and tests.

**Hello Routekit! option.** Want something concrete to start with? Type **demo** and we'll scaffold a small "Hello Routekit!" story so you can watch story-writing and decomposition end to end. Do this in your own project, not the rks framework repo — the demo writes real files. It's an option, not a requirement.

Type your problem statement (or **demo**) and we'll get the PO Governor running.`;

  return makePayload("first_story", state, display);
}

function handleFirstBuild(state, opts, projectRoot) {
  const responses = opts?.responses || {};
  const governorResult = responses.governorResult;
  const storyId = state.responses?.first_story?.storyId;

  if (!storyId) {
    return makePayload(
      "first_build",
      state,
      "Stage 4 result is missing — no story ID found in onboarder state. Please re-run Stage 4 first by calling `/rks-onboard --stage first_story`."
    );
  }

  if (governorResult) {
    if (governorResult.ok === false || governorResult.error) {
      return emitGovernorError(projectRoot, "first_build", governorResult, state);
    }

    emit(projectRoot, "onboarder.stage.started", { stage: "first_build" });
    const t0 = Date.now();

    const rawCost = governorResult.rawCost ?? null;
    const efficientCost = governorResult.efficientCost ?? null;
    const wasteRatio = governorResult.wasteRatio ?? null;
    const filesChanged = governorResult.filesChanged ?? null;
    const inputTokens = governorResult.inputTokens ?? null;
    const outputTokens = governorResult.outputTokens ?? null;
    const modelName = governorResult.modelName ?? null;

    if (!state.responses) state.responses = {};
    state.responses.first_build = { rawCost, efficientCost, wasteRatio, filesChanged, inputTokens, outputTokens, modelName };

    if (!state.completedStages.includes("first_build")) {
      state.completedStages.push("first_build");
    }

    emit(projectRoot, "onboarder.stage.completed", {
      stage: "first_build",
      storyId,
      rawCost,
      efficientCost,
      wasteRatio,
      durationSeconds: Math.round((Date.now() - t0) / 1000),
    });

    let costReport;
    if (rawCost != null) {
      costReport = `**Cost report:**
- Raw cost: $${rawCost.toFixed(4)}
- Efficient cost: $${efficientCost != null ? efficientCost.toFixed(4) : "n/a"}
- Waste ratio: ${wasteRatio != null ? (wasteRatio * 100).toFixed(1) + "%" : "n/a"}

The raw cost is what you'd pay with no caching. The efficient cost is after prompt caching. The waste ratio shows how much of the raw spend was eliminated.`;
    } else {
      costReport = `Cost reporting is not yet available in this version of rks. Check \`runs/\` in your project directory for the raw Governor log.`;
    }

    const display = `Build complete for \`${storyId}\`.

${costReport}

Type **ship** to move to Stage 6 — the Ship Governor will open a PR.`;

    return makePayload("first_build", state, display, { storyId, rawCost, efficientCost, wasteRatio });
  }

  const display = `Stage 5 of 7 — First Build.

The Build Governor will implement \`${storyId}\`. Here is what happens:

1. **Plan** — The planner generates a step-by-step plan scoped to the story's target files.
2. **Review** — You see the plan before it runs.
3. **Exec** — The executor applies the plan file by file, with hooks enforcing scope.
4. **Test** — The test runner verifies acceptance criteria.
5. **Cost** — A cost report shows what this build actually cost.

Type **run** to launch the Build Governor, or \`/build ${storyId}\` directly.`;

  const result = makePayload("first_build", state, display);
  result.pendingDispatch = { type: "build", storyId };
  return result;
}

function handleFirstShip(state, opts, projectRoot) {
  const responses = opts?.responses || {};
  const governorResult = responses.governorResult;
  const storyId = state.responses?.first_story?.storyId || null;

  if (governorResult) {
    if (governorResult.ok === false || governorResult.error) {
      return emitGovernorError(projectRoot, "first_ship", governorResult, state);
    }

    emit(projectRoot, "onboarder.stage.started", { stage: "first_ship" });
    const t0 = Date.now();

    const prUrl = governorResult.prUrl ?? null;
    const prState = governorResult.prState ?? null;
    const commitSha = governorResult.commitSha ?? null;
    const integrationBranch = governorResult.integrationBranch ?? null;

    if (!state.responses) state.responses = {};
    state.responses.first_ship = { prUrl, prState, commitSha, integrationBranch };

    if (!state.completedStages.includes("first_ship")) {
      state.completedStages.push("first_ship");
    }

    emit(projectRoot, "onboarder.stage.completed", {
      stage: "first_ship",
      storyId,
      prUrl,
      prState,
      durationSeconds: Math.round((Date.now() - t0) / 1000),
    });

    let shipRef;
    if (prUrl) {
      shipRef = `**PR:** ${prUrl} (${prState || "open"})`;
    } else if (commitSha) {
      shipRef = `**Commit:** \`${commitSha}\` → \`${integrationBranch}\``;
    } else {
      shipRef = "Ship complete.";
    }

    const display = `Shipped. ${shipRef}

Three things to look at:
1. **The diff** — the actual changes the Build Governor applied.
2. **The cost report** — in the PR body, the full cost breakdown.
3. **CI status** — your pipeline should be running against these changes.

Type **next** to move to Stage 7 — we'll wrap up and show you where to go from here.`;

    return makePayload("first_ship", state, display, { storyId, prUrl, prState, commitSha, integrationBranch });
  }

  const display = `Stage 6 of 7 — First Ship.

The Ship Governor will commit the changes, open a PR, and attach the cost report to the PR body.

Type **ship** to run \`/ship\` now.`;

  const result = makePayload("first_ship", state, display);
  result.pendingDispatch = { type: "ship", storyId };
  return result;
}

function handleNextSteps(state, _opts, projectRoot) {
  emit(projectRoot, "onboarder.stage.started", { stage: "next_steps" });
  const t0 = Date.now();

  const storyId = state.responses?.first_story?.storyId || null;
  const buildData = state.responses?.first_build || null;
  const shipData = state.responses?.first_ship || null;
  const totalDurationSeconds = state.startedAt
    ? Math.round((Date.now() - state.startedAt) / 1000)
    : 0;

  const costLine = buildCostSummary(buildData);
  const prRef = shipData?.prUrl || "n/a";

  const summaryPanel = `**Session summary:**
  Story shipped: ${storyId || "none (stages skipped)"}
  Build cost:    ${costLine}
  PR:            ${prRef}
  Duration:      ${totalDurationSeconds}s`;

  const display = `Stage 7 of 7 — Where to next.

${summaryPanel}

You've seen rks end-to-end. Here's the quick reference:

**Slash commands:**
- \`/po "describe a problem"\` — write a story (Product Owner Governor)
- \`/build <problemId>\` — build an existing story (Build Governor)
- \`/ship\` — commit and open a PR (Ship Governor)
- \`/research "question or task"\` — research, design, or document something
- \`/release patch|minor|major\` — release staging to main with version bump

You can also just describe what you want in chat and the Dispatcher will route you to the right skill.

**Papers worth reading (in priority order):**
1. \`notes/research.2026.04.30.rks-onboarder-design.md\` — onboarder design and rationale
2. \`notes/how-to.agent-operations.3-build.md\` — Build Governor reference
3. \`notes/how-to.child-project-kickoff.md\` — child project setup checklist

**Bookmark:** \`notes/how-to.child-project-kickoff.md\` covers the manual setup steps if you ever need to re-configure a child project from scratch.

**0.x maturity note.** rks is pre-1.0. Behavior can change between releases. Pin to a specific tag in child projects: \`"@routekit/mcp-rks": "github:ux287/routekit-shell#v0.x.y"\`. Check releases at https://github.com/ux287/routekit-shell/releases.

**UX287** — Feedback, questions, and reports: open an issue or start a discussion at https://github.com/ux287/routekit-shell.`;

  // Write completedAt to project.json; preserve completedVersion if already set (e.g. "skipped-as-power-user")
  const proj = readProjectJson(projectRoot);
  proj.onboarder = {
    ...(proj.onboarder || {}),
    completedAt: Date.now(),
    completedVersion: proj.onboarder?.completedVersion || "1.0",
  };
  writeProjectJson(projectRoot, proj);

  if (!state.completedStages.includes("next_steps")) {
    state.completedStages.push("next_steps");
  }
  state.completedAt = Date.now();

  const storiesShipped =
    state.completedStages.includes("first_ship") && (shipData?.prUrl || shipData?.commitSha) ? 1 : 0;
  const stagesSkipped = STAGES.filter(
    (s) => state.completedStages.includes(s) && !state.responses?.[s] && s !== "next_steps"
  );
  const totalTokens = buildData?.inputTokens != null
    ? buildData.inputTokens + (buildData.outputTokens || 0)
    : 0;

  emit(projectRoot, "onboarder.stage.completed", {
    stage: "next_steps",
    durationSeconds: Math.round((Date.now() - t0) / 1000),
  });

  emit(projectRoot, "onboarder.completed", {
    totalDurationSeconds,
    totalTokens,
    storiesShipped,
    stagesSkipped,
  });

  return makePayload("next_steps", state, display);
}

